function calculateDigit(base: string, weights: number[]): string {
    let sum = 0;
    for (let i = 0; i < base.length; i++) {
        sum += parseInt(base[i]) * weights[i];
    }
    const remainder = sum % 11;
    return remainder < 2 ? '0' : (11 - remainder).toString();
}

